function applyLang(lang) {
  const t = translations[lang] || translations.es;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    if (t[el.dataset.i18n] !== undefined) el.textContent = t[el.dataset.i18n];
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    if (t[el.dataset.i18nHtml] !== undefined) el.innerHTML = t[el.dataset.i18nHtml];
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    if (t[el.dataset.i18nPlaceholder] !== undefined) el.placeholder = t[el.dataset.i18nPlaceholder];
  });

  document.documentElement.lang = lang;
  localStorage.setItem('lang', lang);

  document.querySelectorAll('.lang-current-label').forEach(label => {
    label.textContent = lang.toUpperCase();
  });
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });
}

function t(key) {
  const lang = document.documentElement.lang || 'es';
  return (translations[lang] || translations.es)[key] || key;
}

window.t = t;

const dropdowns = document.querySelectorAll('.lang-dropdown');

dropdowns.forEach(dropdown => {
  dropdown.querySelector('.lang-current').addEventListener('click', () => {
    dropdowns.forEach(d => { if (d !== dropdown) d.classList.remove('open'); });
    dropdown.classList.toggle('open');
  });
});

document.addEventListener('click', e => {
  dropdowns.forEach(d => { if (!d.contains(e.target)) d.classList.remove('open'); });
});

document.querySelectorAll('.lang-option').forEach(opt => {
  opt.addEventListener('click', () => {
    applyLang(opt.dataset.lang);
    dropdowns.forEach(d => d.classList.remove('open'));
  });
});

const initLang = localStorage.getItem('lang') || 'es';
applyLang(initLang);
